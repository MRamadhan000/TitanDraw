def save_to_obj(filename):
    with open(filename, 'w') as f:
        # Tulis semua vertices tanpa komentar
        for v in vertices:
            f.write(f"v {v[0]} {v[1]} {v[2]}\n")
        # Tulis semua faces
        for face in faces:
            f.write(f"f {face[0]} {face[1]} {face[2]} {face[3]}\n")

def draw_name():
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT)
    glLoadIdentity()
    gluLookAt(0, 0, 400, 0, 0, 0, 0, 1, 0)  # Kamera di z=400
    
    # Gambar huruf-huruf dan kumpulkan data geometri
    draw_letter_T()
    draw_letter_I()
    draw_letter_T2()
    draw_letter_A()
    draw_letter_N()
    
    # Simpan ke file OBJ
    save_to_obj("titan2.obj")
    
    glFlush()
    glutSwapBuffers()

def setup():
    glClearColor(0.1, 0.1, 0.1, 1.0)  # Latar belakang abu-abu gelap
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    gluPerspective(45, 800 / 600, 0.1, 1000.0)  # Proyeksi perspektif untuk 3D
    glMatrixMode(GL_MODELVIEW)
    glLoadIdentity()
    
    # Aktifkan depth test untuk 3D
    glEnable(GL_DEPTH_TEST)
    
    # Aktifkan antialiasing
    glEnable(GL_BLEND)
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA)
    glEnable(GL_LINE_SMOOTH)
    glEnable(GL_POLYGON_SMOOTH)
    glHint(GL_LINE_SMOOTH_HINT, GL_NICEST)
    glHint(GL_POLYGON_SMOOTH_HINT, GL_NICEST)

