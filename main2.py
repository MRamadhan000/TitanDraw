# List untuk menyimpan vertices, faces, dan komentar untuk setiap vertex
vertices = []
vertex_comments = []
faces = []
vertex_count = 0

def add_vertex(x, y, z):
    global vertex_count
    vertices.append((x, y, z))
    vertex_count += 1
    return vertex_count

def add_face(v1, v2, v3, v4):
    faces.append((v1, v2, v3, v4))

